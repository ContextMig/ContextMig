@LargeTest
@RunWith(AndroidJUnit4.class)
public class ShoppingListTest6 {

    @Rule
    public ActivityTestRule<MainApp> mActivityTestRule = new ActivityTestRule(MainApp.class);

    @Test
    public void shoppingListTest6() {
        onData(anything()).inAdapterView(withId(R.id.lvShoppingList)).
                atPosition(0).onChildView(withId(R.id.nameShoppingList)).perform(longClick());

        DataInteraction linearLayout4 = onData(anything())
                .inAdapterView(childAtPosition(
                        withId(android.R.id.content),
                        0))
                .atPosition(0);
        linearLayout4.perform(click());

        ViewInteraction editText3 = onView(
                allOf(childAtPosition(
                        allOf(withId(android.R.id.custom),
                                childAtPosition(
                                        withClassName(is("android.widget.FrameLayout")),
                                        0)),
                        0),
                        isDisplayed()));
        editText3.perform(replaceText("new-list"), closeSoftKeyboard());

        ViewInteraction button3 = onView(
                allOf(withId(android.R.id.button1), withText("OK"),
                        childAtPosition(
                                childAtPosition(
                                        withClassName(is("android.widget.LinearLayout")),
                                        0),
                                2),
                        isDisplayed()));
        button3.perform(click());

        ViewInteraction textView2 = onView(
                allOf(withId(R.id.nameShoppingList), withText("new-list"),
                        isDisplayed()));
        textView2.check(matches(withText("new-list")));

    }

    private static Matcher<View> childAtPosition(
            final Matcher<View> parentMatcher, final int position) {

        return new TypeSafeMatcher<View>() {
            @Override
            public void describeTo(Description description) {
                description.appendText("Child at position " + position + " in parent ");
                parentMatcher.describeTo(description);
            }

            @Override
            public boolean matchesSafely(View view) {
                ViewParent parent = view.getParent();
                return parent instanceof ViewGroup && parentMatcher.matches(parent)
                        && view.equals(((ViewGroup) parent).getChildAt(position));
            }
        };
    }
}
